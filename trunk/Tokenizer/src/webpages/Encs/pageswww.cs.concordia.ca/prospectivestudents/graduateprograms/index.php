<h2>PhD program</h2>
<p>We have a dynamic doctoral  program which trains students in fundamentals and theory or in practical applications.</p>
<p>Doctoral studies can be pursued in one of the following areas: mobile computing and wireless networks; distributed systems; computer networks and protocols; parallel algorithms and architectures; ad hoc networks; numerical analysis and scientific computing; semantics of programming languages; databases; knowledge-based systems; semantics web; symbolic and algebraic computation; combinatorial algorithms; mathematical programming; artificial intelligence; natural language processing; pattern recognition; image processing; graphics and visualization techniques multimedia computing; bioinformatics; software architecture; requirement engineering; software measurement; user interface; software comprehension and maintenance and dependable software.</p>
<p>A thesis based on this work must be submitted and defended in an oral examination. Find out more about our <a href="http://www.cs.concordia.ca/prospectivestudents/graduateprograms/phdprogram/">PhD program</a>.</p>
<h2>Master's degrees </h2>
<p>In our Department, we offer two types of Master's degrees in Computer  Science and two in Software Engineering. One is based on <strong>course work</strong> preparing the students for broader knowledge and the other is <strong>Thesis-based</strong> which trains students more in  depth on a chosen thesis topic. Find out more about the:</p>
<ul>
<li><a href="http://www.cs.concordia.ca/prospectivestudents/graduateprograms/masterincomputerscience/index.php">Master in Computer Science</a><a href="http://www.cs.concordia.ca/prospectivestudents/graduateprograms/masterofscienceincomputerscience/"></a> - designed to provide students with an opportunity to strengthen their knowledge in a specific area of computer science in the broad sense, and to provide a significant introduction to research. The research results will be presented in a thesis, which must be defended in an oral examination. This program will appeal primarily to students interested in full-time study.</li>
<li><a href="http://www.cs.concordia.ca/prospectivestudents/graduateprograms/masterofappliedcomputerscience/">Master in Applied Computer Science</a> - provides a sound education in the theory and applications of computing so that one can orient towards computer applications in industry and commerce. The program is open to candidates employed in industry or commerce who wish to study on a part-time basis. This program consists of only course work.</li>
<li><a href="http://www.cs.concordia.ca/prospectivestudents/graduateprograms/masterofsoftwareengineering/">Master in Engineering (Software Engineering)</a> - provides a sound education in the theory and practice of software engineering, enabling graduates to work confidently with software engineering applications in industry and commerce. The program is open to candidates engaged in full-time study and also to candidates employed in industry or commerce who wish to study on a part-time basis.</li>
<li><a href="http://www.cs.concordia.ca/prospectivestudents/graduateprograms/masterofappliedsciencesoftwareengineering/">Master in Applied Science (Software Engineering)</a> - designed to provide students with an opportunity to further their knowledge in a specific area of software engineering and to provide a significant introduction to research in software engineering. The research results will be presented in a thesis, which must be defended in an oral examination. This program will appeal primarily to students interested in full-time study.</li>
</ul>
<h2>Graduate diploma</h2>
<p>For preparing students from other  disciplines for graduate level work or for employment we also offer a  one year Post Graduate Diploma program. Find out more about the <a href="http://www.cs.concordia.ca/prospectivestudents/graduateprograms/diplomaincomputerscience/">Graduate Diploma in Computer Science</a>.</p>

