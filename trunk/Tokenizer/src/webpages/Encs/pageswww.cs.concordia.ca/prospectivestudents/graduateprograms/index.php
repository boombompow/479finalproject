<h2>About our department</h2>
<p>Based in the Faculty of Engineering and Computer Science, our department is one of the pioneers in Computer Science education and the largest in the nation. We train our students with care and provide financial support for Thesis students. <a href="../../aboutthedepartment/index.html">About our department</a>.</p>
<h2>Facilities</h2>
<p>Graduate students in the Department of Computer Science and Software Engineering have access to a wide range of laboratory and computing facilities. There are laboratories for:</p>
<ul>
<li>database and information systems</li>
<li>software engineering</li>
<li>distributed systems</li>
<li>computer networks</li>
<li>computational mathematics and visualization</li>
<li>pattern recognition and machine intelligence</li>
<li>computational linguistics</li>
<li>fungal genomics</li>
<li>usability testing</li>
</ul>
<p>The Department also makes extensive use of the university's central computing systems.</p>
<p>Find out more about the department's <a href="../../research/index.html">research groups and facilities</a>.</p>
<h2>Concordia University</h2>
<p>Concordia University is a large, urban, English-language institution, located in Montreal, one of the culturally rich and vibrant cities in North America and one of the world's largest French-speaking cities with a cosmopolitan atmosphere. <a href="http://www.cs.concordia.ca/prospectivestudents/aboutconcordia/">More about Concordia</a>.</p>

