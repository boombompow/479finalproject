<h2>Tour Concordia</h2>
<p>Concordia continues its drive to provide first-class teaching and research facilities, including new buildings and residences. Want to know more about Concordia? Why not take a virtual tour of our two campuses, or, if you intend to be in Montreal, arrange a visit to the university. Take a <a href="http://www.concordia.ca/about/whereweare/tourconcordia/">tour of Concordia</a>.</p>
<h2>Life on Campus</h2>
<p>The university offers a wide range of <a href="http://www.concordia.ca/info/currentstudents/campus_resources/">on-campus resources</a>, such as the many recreation and athletic programs, the student-driven Sustainable Concordia initiative, our libraries, computer and bookstores, art galleries and performance venues, and much more.</p>
<p>A university would not be complete without its <a href="http://www.concordia.ca/info/currentstudents/studentorg/">student associations</a>. Your experience at Concordia will be enhanced by your involvement in  the many varied organizations that contribute to the enrichment of  student life on campus.</p>
<h2>Telling our stories</h2>
<p>One of Canada's most innovative universities, Concordia values inter-disciplinary approaches to learning, and is dedicated to offering the best possible scholarship, research and training. You can learn about our leading-edge teaching and research, our inspired students and our close links to the community in the <a href="http://www.concordia.ca/about/whoweare/tellingourstories/">Telling our stories</a> section.</p>
<h2>Fast facts</h2>
<p>Concordia boasts a diverse student body of over 40,000 undergraduate, graduate and continuing education students from more than 150 countries, studying in over 500 programs. For a snapshot of Concordia in figures, see the <a href="http://www.concordia.ca/about/whoweare/fast_facts/">Fast facts</a> page.</p>
<h2>History</h2>
<p>Concordia's reputation as one of Canada&rsquo;s most dynamic and innovative universities has its roots in over 180 years of pursuing academic excellence and student success. This tradition of individual empowerment, discovery and leadership building was developed by the University&rsquo;s founding institutions - Loyola College and Sir George Williams University - and  continues to flourish locally, nationally and on the global stage. Learn more about our <a href="http://www.concordia.ca/about/whoweare/ourhistory/">rich history</a>.</p>
<h2>Montreal, Quebec, Canada</h2>
<p>Concordia's location in the vibrant, cosmopolitan city of Montreal is a great asset for the university, its students and its staff. Learn more about Concordia's location in the heart of <a href="http://www.concordia.ca/about/whereweare/montreal/">Montreal</a>.</p>
