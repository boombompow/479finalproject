<p>The department is located in the modern and state of the art 
<a href="http://buildings.concordia.ca/sgw/ev.php">Engineering, Computer Science and Visual Arts Integrated 
Complex</a> at the heart of downtown Montreal. Our research facility is comprised of 44 computing laboratories used by our various <a href="../researchgroups/">research groups</a>; the laboratories contain  regularly updated computer systems, which provide a complete setting for our current and potential students as well as visitors to conduct research in a wide range of research fields.</p>
<h2>Teaching Facilities</h2>
<p>Computing facilities for teaching in the department of Computer Science and Software Engineering are provided by the centralized  IT services of the Faculty of Engineering and Computer Science, namely Academic  and Information Technology Services (AITS). Over 1600 PCs are integrated in a gigabit Network, in addition to a 16 terabyte NAS file server, numerous &quot;Services&quot; and &quot;Application&quot; servers, and a modern  HPC Cluster with 464 compute cores and 128 visualization cores.</p>
<p>For undergraduate teaching, the facilities are located in the Hall Building where our department uses over 21 different computer labs with a total capacity of 484 seats/PCs and over 10 project rooms with 17 seats/PCs   to experiment with security, software installation and evaluation, and  implementation of device-drivers.</p>
<p>For graduate teaching, there are about 300 desktops available in 26 computer labs and 4 project rooms  in the EV building.  All systems are dual boot Linux/Windows(XP), with  general Microsoft tools provided by the current Campus agreement and  other special engineering applications (AutoCAD, Matlab, Rational  Rose, etc) necessary to satisfy the teaching mission. The software is  updated every term and the systems are monitored closely.</p>
<h2>Research Facilities</h2>
<p>The  department also has over 15 research laboratories with specialized  hardware and software to support research in different subdisciplines   such as software engineering, mathematical computing, mobile  computing, networks, image processing, computer vision, graphics,  visualization, human computer interaction, gaming, artificial   intelligence, databases, data mining etc. All the equipment is upgraded regularly; computers are usually less than 3 years old and are never more than 5 years old.</p>
