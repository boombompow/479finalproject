<h2>3D-Graphics</h2>
<p>The primary focus of research in this lab is the development of new, efficient and scalable computational techniques for 3D graphics and more generally for computing with visuals. More about <a href="http://www.cs.concordia.ca/research/researchgroups/3d-graphics">3D-Graphics</a>.</p>

<h2>Bioinformatics</h2>
<p>Research in this area involves the design, evaluation, and implementation of algorithms, databases and systems in bioinformatics, including ontologies and the semantic web, user interfaces for bioinformatics tools and systems. <a href="http://www.cs.concordia.ca/research/researchgroups/bioinformatics">More about Bioinformatics</a>.</p>
<h2>Concordia Computational Combinatorial Optimization Laboratory</h2>
<p>ConCoCO's immediate research agenda revolves around branching rules in combinatorial optimization and computing  Van der Waerden numbers. <a href="http://www.cs.concordia.ca/research/researchgroups/concoco">More about ConCoCO</a>.</p>
<h2>Computational Linguistics at Concordia </h2>
<p>The lab is interested in all aspects of Natural Language Processing: from theoretical research of linguistic phenomena such as coreference to the development of specific applications such as automatic summarizers or question-answering systems. <a href="http://www.cs.concordia.ca/research/researchgroups/clac/">More about CLaC</a>.</p>
<h2>Database Research Group</h2>
<p>The current areas of focus in database research are on modeling, languages, query processing and optimization and include, in random order, Semi-structured Data and XML, Data Warehousing, OLAP, and Data Mining, Logic Programming and Deductive Databases, Semantic Web, Digital Libraries, and much more. <a href="http://www.cs.concordia.ca/research/researchgroups/dbrg/">More about the Database Research Group</a>.</p>
<h2>Dependable Software Research Group</h2>
<p>The DSRG engages in theoretical and applied research in the areas of Software Engineering and Formal Methods - in particular, Software Requirements, Model Driven Design (MDD) and Module/Behavioral Interface Specification. <a href="http://www.cs.concordia.ca/research/researchgroups/dsrg/">More about the DSRG</a>.</p>
<h2>Human-Centered Software Engineering</h2>
<p>This multidisciplinary area of research involves researchers from computer science, psychology, communication and art. Major investigations include agent technology, cognitive and modeling; user interfaces design, user-centered development of interactive systems; user-oriented tests, usability evaluation; software and information visualization techniques. <a href="http://www.cs.concordia.ca/research/researchgroups/hcse/">More about HCSE research</a>.</p>
<h2>Large Scale Optimization Systems </h2>
<p>The Large Scale Optimization Systems Laboratory focuses on      optimization techniques aiming at solving large scale systems with      operations research techniques (e.g., metaheuristics and      decomposition techniques) arising in different engineering areas      such as communication networks (e.g., network design, network      provisioning, optical grid dimensioning) and industrial engineering      (staff scheduling, train scheduling, reasoning under uncertainty in      expert systems, clustering or data mining, optimization of      multi-agent systems).&nbsp;<a href="http://www.cs.concordia.ca/research/researchgroups/largescaleoptimizationsystems/index.php">More about Large Scale Optimization Systems</a>. </p>
<h2>Mathematical Computing and Visualization</h2>
<p>Research in this area includes algebraic and combinatorial computing, coding theory, computational algebra and computational number theory, combinatorial optimization, computational geometry, computational group theory, computer graphics, cryptography, numerical and nonlinear analysis, mathematical modeling, scientific and information visualization, visual computing, virtual environments. <a href="http://www.cs.concordia.ca/research/researchgroups/cmv/">More about Mathematical Computing and Visualization</a>.</p>
<h2>Networks</h2>
<p><a href="http://www.cs.concordia.ca/research/researchgroups/networks/">More about Networks research</a>.</p>
<h2>Parallel and Distributed Computer Systems</h2>
<p>This broad area of research has several subgroups of people working as research teams in areas such as: algorithms for parallel computing, wireless and distributed computing, protocols for secure multicasting and QoS signaling, network resiliency, distributed agent based architectures for fault tolerance and performance enhancement, pattern based parallel programming, predicate detection and slicing of distributed programs, object oriented parallel programming systems. More about <a href="http://www.cs.concordia.ca/research/researchgroups/pdcs/">Parallel and Distributed Computer Systems</a>.</p>
<h2>Software Engineering</h2>
<p>Both formal, empirical and practical software engineering research is currently going on in the following areas: requirements engineering, analysis, formal methods; object-oriented reusable software, software design patterns and architectures; software maintenance, comprehension and visualization; software measurement; software usability engineering, human factors in software development, developer-centric CASE tools, empirical studies; specification, verification and testing; reactive systems. <a href="http://www.cs.concordia.ca/research/researchgroups/soen/">More about Software Engineering research</a>.</p>
<p>&nbsp;</p>
<h2> Software Maintenance and Evolution Research Group </h2>
<p> The group investigates problems with the object-oriented and aspect-oriented programs. <a href="http://hypatia.cs.concordia.ca/">More about this Reaserch Group.</a> </p>
