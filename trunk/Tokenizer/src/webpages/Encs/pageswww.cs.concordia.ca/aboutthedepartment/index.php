<h2>History</h2>
<p>The department is one of the oldest and largest in the nation. Created after the establishment of Concordia University by the merger of the Loyola Computer Science Department (established in 1970) and the Sir George Williams Department of Computer Science (established in 1972), the Loyola Computer Science Department was the first to offer a Computer Science degree program in Quebec. The department is now part of the <a href="../../pages/index.html">Faculty of Engineering and Computer Science</a>.</p>
<h2>Teaching</h2>
<p>We offer <a href="../prospectivestudents/index.html">degree programs</a> in Computer Science as well as in Software Engineering. Two different 
Masters Degree programs are available. One is based on thesis work and the other 
entirely on course work. We have a dynamic doctoral program and a Graduate 
Diploma program geared for retraining graduates from other disciplines into 
computer science. We serve both full time and part-time student communities, and 
offer suitable financial assistance in the form of bursaries and sholarships. We are very proud of the unique orientation of our academic
programs &ndash; practical and flexible with lasting foundational
knowledge, matching well with the needs of today&rsquo;s information
technology driven society. We have over 
1000 undergraduate students and over 600 graduate students studying in our 
department.</p>
<h2>Research</h2>
<p>We have <a href="http://www.cs.concordia.ca/people/faculty/full-time/index.php">37 full-time faculty members</a></a>,  <a href="http://www.cse.concordia.ca/people/faculty/emeritusprofessors/index.php"> several emeritus professors</a>, as well as <a href="http://www.cs.concordia.ca/people/faculty/adjunctprofessors/index.php">adjunct professors</a> and <a href="http://www.cs.concordia.ca/people/faculty/part-timefaculty/index.php">part-time faculty</a>. The department is supported by a dedicated 
administrative staff team. Combined, our faculty members receive over a million dollars 
in research grants from federal, provincial and industrial sources. More about <a href="../research/index.html">research</a>.</p>
<h2>Facilities</h2>
<p>The <a href="http://www.cs.concordia.ca/research/facilities/">computing facilities</a> are broadly classified into 
teaching and research-oriented. The <a href="http://www.encs.concordia.ca/aits/public/top/labs">teaching 
labs</a> are used by both undergraduate and graduate students for 
their course work. In addition, several research groups and labs performing 
innovative and cutting-edge research are housed 
in the new 
Engineering, Computer Science and Visual Arts Integrated 
Complex.</p>
