   <p>The Dean of Students Office is the principal resource for the interpretation and regulation of policies and procedures as these apply to the non-academic aspects of student life. Student associations are one of the primary means by which students can relate meaningfully to the institution. By providing liaison with and support to student groups and their governing bodies, the Dean of Students encourages students in the conduct of their own collective affairs, and facilitates the growth of student associations and the active involvement in them by students.</p>  <p>The Dean of Students Office is:</p> <ul>     <li>The office to contact to register your organization</li>     <li>One of the places where you can book space for your student organization</li>          <li>An information centre (Loyola Campus) with computers for student use</li>     <li>Where you apply for funding for your project</li>     <li>Where you'll find a commissioner of oaths</li>          <li>Where you call if you need a tutor or would like to become one</li>     <li>Where you can go if you want to talk about your student association</li>     <li>Where you will find resources and people to help you make sense of your concerns about the quality of student life at the university.</li> </ul>  <p><strong>Please call or email us with your questions or concerns!</strong>&nbsp;</p><div>&nbsp;</div><div>Loyola:  marburns@alcor.concordia.ca 514-848-2424 extension 4239&nbsp;</div><div>SGW:    dofs@alcor.concordia.ca 514-848-2424 extension 3517</div>
  

