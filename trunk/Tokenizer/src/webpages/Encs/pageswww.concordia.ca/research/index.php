<p>Faculty research interests are diverse, ranging from the new rural economy and
  development to race and ethnic relations, comparative social history, social
  theory, and cultural sociology. Some examples of specific projects include the
  social, political and cultural impact of globalization; sex work; comparative
  urban cultures; the role of information technologies in mediating surveillance
  relations; men and masculinities; digital culture and video games; citizenship
  and transculturalism; the history of Canadian sociology; fear, risk and
  governmentality; minority religious groups; global development and labour
  institutions; transformations in the family. Canadian and Quebec society provide
  major but not exclusive research foci.</p>
<p>Students have a long tradition of involvement in research - as part of faculty projects and on their own through student-initiated projects, theses, and essays. Our faculty are well-known for their encouragement of student participation in research: locally, nationally, and internationally.</p>
<p>Examples can be found via the links above - or check out our '<a href="../alumni/index.php">Great Grads</a>' to see how our department has prepared students for exciting careers.</p>
