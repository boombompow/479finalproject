<h1>Recent Books</h1>
<p><span class="red" style="font-weight: bold">Maximillian Forte</span> edited a collection of essays entitled <a href="http://www.peterlang.com/index.cfm?event=cmp.ccc.seitenstruktur.detailseiten&seitentyp=produkt&pk=53723&concordeid=310102" target="_blank">Indigenous Cosmopolitans: Transnational and Transcultural Indigeneity in the 21st Century</a>.</p>
<p>This collection challenges the assumption that indigeneity and transnationalism are conflicting conditions..</p>
<div class="hr">&nbsp;</div>
<p><span class="red" style="font-weight: bold">Sally Cole</span> compiled, edited, and introduced stories told by Maggie Wilson in <a href="http://www.nebraskapress.unl.edu/product/Rainy-River-Lives,674098.aspx" target="_blank">Rainy River Lives</a>, University of Nebraska Press, 2009.</p>
<p>This is a collection of long-lost stories of Ojibwe men and women as told by traditional storyteller, Maggie Wilson.</p>
<div class="hr">&nbsp;</div>
<p>Louise Guyon, Nicole April, <span class="red" style="font-weight: bold">Sylvia Kairouz</span>, Elisabeth Papineau, and Lyne Chayer <a href="http://www.pulaval.com/catalogue/tabac-alcool-drogues-jeux-hasard-argent-9358.html" target="_blank">Tabac, alcool, drogues, jeux de hasard et d'argent. &Agrave; l'heure de l'int&eacute;gration des pratiques</a> Les Presses de l'Univerit&eacute; Laval, 2009.</p>
<p>This volume explores the relationship among the four addictions - providing up-to-date information regarding their commonalities and exploring strategies for prevention..</p>
<div class="hr">&nbsp;</div>
<p><span class="red" style="font-weight: bold">David Howes</span>:</span> <a href="../../pageswww.bergpublishers.com/_tabid_5216" target="_blank" style="font-style: italic">The  Sixth Sense Reader</a>. Oxford: Berg Publishers, 2009.</p>
<p>This is the final volume in the Sensory  Formations series. What is the sixth sense? you  ask. Well, you will either have to use your sixth sense or&nbsp;read the book  to find out.</p>
<div class="hr">&nbsp;</div>
<p><span class="red" style="font-weight: bold">Nirmala Bains</span> (with <span class="red" style="font-weight: bold">Martha Elvir</span>, <span class="red" style="font-weight: bold">Ya-Ling Fan</span>, <span class="red" style="font-weight: bold">Sarah Orr</span>, and <span class="red" style="font-weight: bold">Miriana Vetere</span>) <span style="font-style: italic"><a href="http://socianth.concordia.ca/pages/StoriesFromMontreal4.php">Stories from Montreal 4</a></span> Tulipe Press, 2009.</p>
<p>This book is the fourth in the series of undergraduate student ethnographies from our department.</p>

<div class="hr">&nbsp;</div>

<p><span class="red" style="font-weight: bold">Daniel Dagenais</span>:</span> <em><a href="http://www.ubcpress.ubc.ca/search/title_book.asp?BookID=4560">The (Un)Making of&nbsp;the Modern Family</a>.&nbsp; </em>UBC       Press Publishers, 2008.&nbsp; </p>

<p>This <strong>book is the translation</strong> of Dr.  Dagenais' book <em>La fin de la famille moderne</em>, which was  published in 2000&nbsp;by Les Presses de l'Universit&eacute; Laval and was awarded the  2000-2001 Prix Jean-Charles Falardeau for the best book publisehd in French in Canada  in the field of social sciences.&nbsp; </p>

<div class="hr">&nbsp;</div>

<p><span class="red" style="font-weight: bold">Danielle Gauvreau</span> (with Diane Gervais &amp; Peter Gossage) (eds): <em><a href="http://www.editionsboreal.qc.ca/fr-result_isbn.php?id=1542">La F&eacute;condit&eacute; des Qu&eacute;b&eacute;coises 1870-1970</a>.</em>Montr&eacute;al: Bor&egrave;al, 2007.</p>

<p>This book was <strong>short-listed for the 2007 John A. Macdonald Prize</strong> of the Canadian  Historical Association, <br /> <strong>as well among the top 10 finalist for the Prix  Jean-Charles-Falardeau</strong> for the best French-language book in the Social  Sciences.&nbsp; Final decision for both prizes  is pending.&nbsp;&nbsp; </p>

<div class="hr">&nbsp;</div>

<p><span class="red" style="font-weight: bold">Ross Higgins</span>:</span> <em><a href="http://www.arsenalpulp.com/bookinfo.php?index=276">Peter Flinsch The Body in Question</a></em>.&nbsp; Montreal: Arsenal       Pulp Press, 2008</p>

<div class="hr">&nbsp;</div>

<p><span class="red" style="font-weight: bold">Greg M. Nielsen</span>: <em><a href="http://www.zedbooks.co.uk/book.asp?bookdetail=4219">Acts of Citizenship</a></em>.       Co-edited with Engin Isin. London: Zed Publications, 2008. </p>

<div class="hr">&nbsp;</div>

<p><span class="red" style="font-weight: bold">Jean-Philippe Warren</span>:</span> <em><a href="http://www.editionsboreal.qc.ca/fr-result_isbn.php?id=1591">Une Douce Anarchie&nbsp;: Les Ann&eacute;es 68 au  Qu&eacute;bec</a>.&nbsp; </em>Montreal: Bor&eacute;al, 2008; and <em><a href="http://www.edvlb.com/showGuidePage.asp?CodeProduit=332706">Ils  voulaient changer le monde. Le militantisme marxiste-l&eacute;niniste au Qu&eacute;bec</a></em>. Montr&eacute;al,  VLB, 2008</p>


