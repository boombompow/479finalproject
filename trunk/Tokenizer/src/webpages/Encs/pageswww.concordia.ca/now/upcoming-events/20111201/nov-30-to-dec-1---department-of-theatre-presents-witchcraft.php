<div class="now_story_subhead">Production revisits Joanna Baillie's history of witchcraft trials</div><p><i>Witchcraft</i> revisits the history of witchcraft trials in Joanna Baillie's homeland of Scotland through a conventicle of destitute female characters seeking power, love, and retribution in an elusive and illusory dark devil. The play explores mass hysteria and paranoia in witches and their accusers, in women and men, and examines how individuals struggle to navigate and negotiate in a culture of fear.</p><p>The play is an important artistic and historical precursor for other dramatic representations of witchcraft, such as Arthur Miller's <i>The Crucible</i>. Co-directors Louis Patrick Leroux and Christina Iovita's staging brings Baillie's drama into the contemporary register, layering live performance with video projection and other multimedia elements as one way to negotiate the temporal and aesthetic distance of the play.<br />&nbsp; <br /><b>When:&nbsp;</b> Wednesday, November 30 at 8 p.m.<br />Thursday, December 1 at 8 p.m.<br />Friday, December 2 at 8 p.m.<br />Saturday, December 3 at 2 and at 8 p.m.<br />Sunday, December 4 at 2 p.m.<b><br />Where:</b>&nbsp; D.B. Clarke Theatre, Henry F. Hall Building (1455 De Maisonneuve Blvd. W.), Sir George Williams Campus<br /><b>Cost: </b>$10 regular, $5 for students and seniors</p><p><b>Box office:<br /></b>No reservations. Tickets available in person only.<br />&bull; &nbsp; Oscar Peterson Concert Hall, 7141 Sherbrooke St. W., Monday to Friday, 10 a.m. to 4 p.m.<br />&bull;&nbsp;&nbsp; D.B. Clarke Theatre, 1455 De Maisonneuve Blvd. W., Monday, Wednesday and Thursday, 12 to 2 p.m.<br />&bull; Tickets also available at the door.<br /><br /><b>Related links:<br /></b>&bull;&nbsp;&nbsp;&nbsp; <a href="http://theatre.concordia.ca">Department of Theatre</a><br /><br /><br /><br />&nbsp;</p>

