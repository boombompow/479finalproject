<img src="http://chem.concordia.ca/custom/images/Capo_top10b.jpg" class="splash2" height="84" width="186" align="left" hspace="20">

<p>An Article by <b>John Capobianco</b> and his collaborators and students in the Royal Society of Chemistry journal, <i>Nanoscale</i>, entitled "Intracellular imaging of HeLa cells by non-functionalized NaYF<sub>4</sub>:Er<sup>3+</sup>, Yb<sup>3+</sup> upconverting nanoparticles" (2010, <b>2</b>, 495; DOI: <a href="http://www.rsc.org/Publishing/Journals/NR/article.asp?doi=b9nr00236g">10.1039/b9nr00236g</a>) received 194 on-line accesses during April 2010, placing it among the <a href="http://www.rsc.org/Publishing/Journals/nr/top10.asp">top 10</a> on this basis.
 </p>
 
<hr style="{color: #CCCCCC; height:1pt}">
<p>Also, recently two of our research groups, and one former faculty member, have had aspects of their research selected for special recognition in prestigious journals.</p>
<hr style="{color: #CCCCCC; height:1pt}">

<img src="http://chem.concordia.ca/custom/images/Lawrence_cover_tnail.jpg" class="splash2" height="84" width="100" align="left" hspace="20">

<p><b>Prof. M. Lawrence</b> and his coworkers, including student Concordia graduate student, <b>Arghavan Shabani</b>have been featured in a <i>Research Profile</i> in a recent issue of the American Chemical Society Journal, Analytical Chemistry: <a href="http://pubs.acs.org/doi/full/10.1021/ac802346w" target="_new">"Bacteria Exposed"</a>, E. Gebel, <i>Anal. Chem.</i>,<b>2009</b>, 81(1), <i>5</i>. The article which inspited this profile, "Bacteriophage-Modified Microarrays for the Direct Impedimetric Detection of Bacteria", was published in the previous issue: <i>Anal. Chem.</i>, 2008, <b>80</b> (24), pp 9475-9482 <b>DOI</b>: 10.1021/ac801607w.</p>

<hr style="{color: #CCCCCC; height:1pt}">

<img src="http://chem.concordia.ca/custom/images/Capo_cover_tnail.jpg" class="splash2" height="84" width="100" align="left" hspace="20">

<p><b>Prof. J. Capobianco</b> and his coworkers, including postdoc <b>Venkataramanan Mahalingam</b>, and two Concordia graduate students, <b>Fiorenzo Vetrone</b> and <b>Rafik Naccache</b>, have published an <a href="http://www.rsc.org/Publishing/Journals/JM/article.asp?doi=b900300b" target="_new">article</a> entitled "Structural and optical investigation of colloidal Ln<sup>3+</sup>/Yb<sup>3+</sup> co-doped KY<sub>3</sub>F<sub>10</sub> nanocrystals" which was selected as the cover article in a recent issue of the Journal of Materials Chemistry, a publication of the Royal Society for Chemistry: <i>J. Mater. Chem.</i>, 2009, <b>19</b>(20), 3149 - 3152, <b>DOI</b>: 10.1039/b900300b</p>

<hr style="{color: #CCCCCC; height:1pt}">

<img src="http://chem.concordia.ca/custom/images/Serpone_cover_tnail.jpg" class="splash2" height="84" width="100" align="left" hspace="20">

<p>Concordia Professor Emeritus <b>Nick Serpone</b> with his co-author, Vyacheslav N. Kuznetsov, has had an upcoming publication selected as a feature article in the American Chemical Society, Journal of Physical Chemistry C. The article: "On the Origin of the Spectral Bands in the Visible Absorption Spectra of Visible-Light-Active TiO<sub>2</sub> Specimens Analysis and Assignments", has appeared in the print edition of the Journal: <i>J. Phys. Chem. C</i>, <b>2009</b>, <i>113</i>, 15110, and may be found on-line: <a href="http://pubs.acs.org/doi/abs/10.1021/jp901034t">DOI: 10.021/jp901034t</a>.</p>
 
