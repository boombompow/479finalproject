				<h3>Content</h3>
<p>The content of this website has been collected, written and edited by the staff of Concordia's Web Communications unit, which is part of Internal &amp; Web Communications, itself a unit within University Communication Services.</p>
<p>The news items are maintained by the editor of <a href="now/">Concordia NOW news &amp; events</a>, which is also under the responsibility of Web Communications. Event information may come from NOW news &amp; events from the <a href="events/">Daily Events calendar</a>, which is maintained by the Information Services unit within <a href="http://web2.concordia.ca/mrkcom/">Marketing Communications</a>. <a href="http://www.concordia.ca/now/media-relations/press-releases/">Official press releases</a> are produced by the <a href="http://www.concordia.ca/now/media-relations/">Media Relations Office</a>.</p>

<p>Much of the content is drawn from official publications, such as the university calendars, viewbooks, student guides, etc. Administrative offices that have contributed content include Enrolment Services, the <a href="../pagesvoip.concordia.ca/">Office of the Registrar</a>, the <a href="">Office of Research</a>, the <a href="http://www.concordia.ca/programs-and-courses/academic-units/">Faculties</a> and <a href="">School of Graduate Studies</a>, the <a href="http://web2.concordia.ca/Inst_Res/IR/IR_homepage.html">Office of Institutional Planning</a>, <a href="../pagesarchives3.concordia.ca/">University Archives</a>, <a href="">Counselling and Development</a>, and the <a href="">Advancement and Alumni Relations office</a>.</p>

<h3>Design</h3>
<p>Concordia's Web Communications unit created the new design and coded the HTML/CSS for the main university website. The new design was also used to create a template for the websites of academic departments, administrative units, official events, etc (e.g., see <a href="http://www.concordia.ca/now/media-relations/">Media Relations</a>). The code for the site and template adheres to XHTML and CSS web standards, and also includes a number of features for <a href="http://www.concordia.ca/web/accessibility/">web accessibility</a>.</p>
<p>The Web Communications unit received valuable feedback during the design process from the Concordia community, including the Marketing Communications unit, members of the Web Forum, some volunteer test subjects, and various faculty and staff who attended presentations given by the unit. </p>
<h3>Technical Support</h3>
<p>The <a href="../pagesvoip.concordia.ca/">IITS Web Group</a>  provides technical support for  server administration, as well as some database and web programming.</p>

<h3>Photography</h3>
<p>Unless otherwise noted, the  photos that appear within the website are from the <a href="http://web2.concordia.ca/mrkcomm/photofinder.php">University Communications Services Photofinder</a> image bank.</p>
			This site was designed and developed by the Faculty of Arts & Science Academic Technology 
				Development team (Christine Daviault and Cameron Campbell) based on the template mentioned above.
