<p><a name="Volume4" id="Volume4"></a></p>
<h3>Volume 4: Table of Contents</h3>
<p style="font-weight: bold">Miriana Vetere: <span style="font-style: italic">Identity in the Italian Community of Saint-Leonard</span></p>
<ul>
  <li>What makes us Italian? A journey into notions of national, community, and personal identity</li>
</ul>
<p style="font-weight: bold">Tamara Tashi McCarthy: <span style="font-style: italic">Les langues du Montr&eacute;al: Following French and English from everyday life to employment opportunities</span></p>
<ul>
  <li>A brief history of the French language and consequent tensions are presented through the experiences of the ethnographer.</li>
</ul>
<p style="font-weight: bold">Catherine Kendler: <span style="font-style: italic">Urban Gathering: Explorations with Montreal's dumpster divers</span></p>
<ul>
  <li>What is dumpster diving and who engages in it? Submerge yourself in garbage cans to get a better understanding of food politics.</li>
</ul>
<p style="font-weight: bold">Sumayya Ayoub: <span style="font-style: italic">Duality: the fine balance between sexuality and Arab tradition</span></p>
<ul>
  <li>&quot;Coming out&quot; in North America does not entail coming out at home. Discover how some gay Arab men negotiate their sexual identity between two worlds.</li>
</ul>
<p style="font-weight: bold">Dafna Israel: <span style="font-style: italic">On Campus Political Space: Behind the scenes of Cinema Politica screening for awareness</span></p>
<ul>
  <li>A backstage look at the organizers, the filmmakers, and the participants of this on-campus film screening event.</li>
</ul>
<p style="font-weight: bold">Ya-Ling Fan: <span style="font-style: italic">Me and the Library</span></p>
<ul>
  <li>An auto-ethnography written through the discovery of the differences that can bring people together in a shared setting.</li>
</ul>
<p style="font-weight: bold">Marth A. Elvir Prieto: <span style="font-style: italic">Women, Children, and the Playgrounds of Montreal</span></p>
<ul>
  <li>The culture of motherhood explored through outings to the local playground. Commonalities are unravelled through the experience of mothers as it intersects with the love for place and a stage in the journey into motherhood.</li>
</ul>
<p style="font-weight: bold">Elisabeth Callahan: <span style="font-style: italic">No Fixed Meaning: An ethnographic photo essay</span></p>
<ul>
  <li>Why did you get that tattoo? The cultural phenomenon of tattoos is explored in a collaborative undertaking.</li>
</ul>
<p style="font-weight: bold">Rayah Al-Mashal: <span style="font-style: italic">An Arabian Cultural Juxtaposition: &quot;Negotiating identities and ascertaining equilibrium&quot; in multi-sited social communities in Canada</span></p>
<ul>
  <li>A nattartive-based literary approach that illustrates the life experiences of three Arab Muslim women studying in Montreal.</li>
</ul>
<p style="font-weight: bold">Natalie Blais: <span style="font-style: italic">The Herouxville Declaration: Accommodation hearings: What is &quot;reasonable accommodation&quot; of &quot;others&quot;?</span></p>
<ul>
  <li>Individuals' perceptions and understandings of accommodation and intercultural differences are explored.</li>
  </ul>
