<p>The Sociology Honours projects were presented to the department on April 13th. Presentations can be viewed by clicking on the relevant links below.</p>
<p><strong>SESSION 1: 9:00 to 10:30 AM</strong> </p>
<ul>
  <li>Morgan  Buck: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/MorganBuckThesis_Powerpoint[1].pdf">Alternatives to Hunger: Evaluating alternative food networks in  low-income urban communities</a></li>
  <li>Sabrina van Duyse:  <a href="http://socianth.concordia.ca/documents/HonoursPresentations/SabrinaVanDuyseFighting Your Way Out HONOURS PRESENTATION.pdf">Fighting your way out: the influence of boxing on at-risk youth</a> </li>
  <li>Kevin Naimi: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/KevinNaimiThesis Presentation 3.pdf">Value Systems and Knowledge</a> </li>
  <li>Francine Small: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/FrancineSmallundergrad thesis presentation 2010 1.pdf">The Representation of Race and Ethnicity in Biomedical  Research</a> </li>
</ul>
<p>&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u>Discussant</u>:  Satoshi Ikeda</p>
<p><strong>SESSION 2: 10:45 AM to 12:15 PM</strong> </p>
<ul>
  <li>Cynthia  Ciocca: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/CynthiaCioccaAcculturation and the Family presentation.pdf">The Effect of Acculturation Differences on Family Relations</a></li>
  <li>Michelle Delisle: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/MichelleDelisleThe Dating Scene Presentation.pdf">The Dating Scene: Individualism and Implications</a> </li>
