  <li>Peter-James Nicoll: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/PeterJamesNicollAnon presentation.pdf">Information  Kryptonite: Anonymous and Internet-based activism in the age of Web 2.0</a> </li>
  <li>Chelsea Peterdy: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/ChelseaPeterdyfinal thesis presentation.pdf">The  Pursuit of Efficiency: How women construct their bodies, health, and fitness in  postmodern society</a> </li>
