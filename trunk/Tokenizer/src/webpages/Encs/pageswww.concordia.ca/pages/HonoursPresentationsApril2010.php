  <li>Jennifer Saad: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/JenniferSaadPres2.pdf">Motherhood and Domestic Abuse: The Impact of Children on  a Woman&rsquo;s Decision to Leave</a></li>
  </ul>
<p><u>Discussant</u>: Danielle Gauvreau</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>***** Pot-Luck Lunch   *****</strong></p>
<p><strong>SESSION 3: 1:30 to 3:00 PM</strong> </p>
<ul>
  <li>Cristina Ispas:  <a href="http://socianth.concordia.ca/documents/HonoursPresentations/CristinaIpsasPresentation Thesis, April 13th.pdf">Patterns of Acculturation of Romanian Immigrants into Canadian Society: Montr&eacute;al</a> </li>
  <li>Stacy Pomroy:  <a href="http://socianth.concordia.ca/documents/HonoursPresentations/StacyPomroy409 thesis presentation.pdf">Discourse on Cosmetic Surgery as Reflected in Nip/Tuck</a> </li>
  <li>Jillian Kropf:  <a href="http://socianth.concordia.ca/documents/HonoursPresentations/JillianKropfTranssexual Embodiment[2].pdf">Transsexual Embodiment: Exploring the Connection Between the Body and the Self </a></li>
  <li>Jordan Venne: <a href="http://socianth.concordia.ca/documents/HonoursPresentations/Jordan Venne - honours thesis.pdf">The  Relationship between Cohesion and Retention for Army Reservists</a> </li>
</ul>
<p>&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u>Discussant</u>:  Jean-Philippe Warren</p>
