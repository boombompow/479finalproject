                            <p><span style="font-weight: bold">SASU</span> is screening <strong>'Subtext:  real stories' by Eric Weissman</strong> on Saturday, March J.A.
                              De
                              Seve
                              Theatre
                              (LB-&shy;&#8208;125)
                              Library
                              Building
