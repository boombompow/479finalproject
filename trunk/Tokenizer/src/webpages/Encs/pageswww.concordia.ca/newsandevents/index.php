                            <p>The free screening will be followed by a discussion with the filmmaker.</p>
                            <p>                              Subtext is a rough-cut documentary exploration. It explores an  urban shantytown and the strides of activists, ex-residents, and the City of Toronto  to find solutions to homelessness. Its focus is the residents of Tent City. The  subjects dictate where cameras go, leading us into their pasts, conflicts, and  daily lives -- by road and foot through the urban landscape.&nbsp; We examine  caves on the Don River, squats under the highway, trips to the hospital, and  abandoned buildings, but our journey always leads back to Tent City &hellip; to a  thorough, often surprising understanding of the people who lived there.</p>
                            <p>                              Eric  Weissman is currently a SIP doctoral student in Sociology &amp; Anthropology at  Concordia University.</p>
