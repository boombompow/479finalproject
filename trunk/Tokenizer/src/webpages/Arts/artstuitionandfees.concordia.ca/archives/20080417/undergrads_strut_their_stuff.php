<p>The over 80 students who presented posters, papers and PowerPoint presentations on April 4 were articulate, enthusiastic and engaged with their material.</p>

<p>And not a single one had completed their first post-secondary degree.</p>

<p>The second annual Arts and Science Undergraduate Research Day showcased a range of knowledge-building across the entire faculty. More than one person has said that the striking thing was the diversity demonstrated here today. Its a characteristic of this faculty that makes us unique, said Associate Dean of Research and Graduate Studies Graham Carr.</p>

<p>Student presentations were divided into three categories: humanities (including research creation), social sciences and natural and health sciences. Poster presentations filled the LB Atrium. Student video and digital work was on view at the back of the Atrium. Oral presentations were scheduled simultaneously throughout the day in the adjacent J.A. DeSève Cinema.</p>

<p>Students presented posters on the architectural heritage of Concordias buildings, reader responses to the <em>Globe and Mail</em>s health reporting and circus performance. Oral presentations ranged from to Amelie DAstouss analysis of the traits that put mammals at risk for extinction to Columbian literature.</p>

<p>All of the work was adjudicated by 21 professors, representing the various departments. They evaluated the material and determined the best oral and the best poster presenters in each of the three categories. The awards were presented at the end of the day by Joanne Locke, Interim Dean of the faculty.<br />
Carr acknowledged the efforts of the faculty supervisors behind the scenes who helped students with their presentations. That kind of collaboration is an essential function of the teaching mission.</p>

<p>In fact, Carr pointed out that the event breaks down the dichotomy of research versus teaching, and instead demonstrates the symbiosis of the two roles.</p>

<p>History student Amie Wrights presentation won the oral prize for humanities, with Mattyas Huggard from Classics, Modern Languages and Linguistics taking the poster prize in that category.</p>

<p>Carina Forans research action project identifying existing systemic gaps in research on the sexual health of young women in Canada took top honours in the social sciences oral presentation category. Alexander Heggies presentation on religious violence and memory earned first place for the Religion Department in the poster category.</p>

<p>Finally Jason Reinglass oral presentation won for natural and health sciences presentation. His research on quadriceps strength for cancer patients had already won him a Young Investigators award in Florida earlier this year (see the <a href="http://cjournal.concordia.ca/archives/20080117/exercise_scientists_involved_in_cancer_therapy.php" target="_blank"><em>Journal</em>, Jan. 17, 2008</a>). Tara Millman took the poster prize in that category for her research on behaviours of low-birth weight infants when interacting with their mothers.</p>

