  <h2>Degree requirements and procedures</h2>
  <h3>What are the requirements to complete for my degree?</h3>
  <p>Get informed about:</p>
  <ul>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/generalrulesandregulations/">General rules and regulations</a></li>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/gparequirement/">GPA requirements</a></li>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/generaleducationrequirements/">General education requirements</a></li>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/registration/">Registration process</a></li>
  </ul>
  <h3>What are the procedures for:</h3>
  <ul>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/concentrationanddegreechanges/">Changing concentration or degree?</a></li>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/readmission/">Applying for readmission?</a></li>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/transfercredits/">Transferring credits?</a></li>
    <li><a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/withdrawal/">Withdrawing from my degree?</a></li>
  </ul>
  <p>Many procedures require a <a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/studentrequestforms/">Student Request Form</a>. </p>
  <h3>Find out more - talk to your <a href="http://artsandscience.concordia.ca/currentstudents/degreerequirementsandprocedures/departmentaladvisors/">departmental advisor</a>.</h3>
  <h2>New students</h2>
  <p>If you are new to Concordia, find out about registration procedures, orientation activities and more. Get more <a href="http://artsandscience.concordia.ca/currentstudents/informationfornewstudents/">information for new students</a>.</p>
  <h2>Exchange students </h2>
  <p>Discover the possibilities of student exchanges. Find out about the eligibility requirements, procedures and bursaries available for <a href="http://artsandscience.concordia.ca/currentstudents/studyabroad/">exchange students</a>.</p>
  <h2>Student services</h2>
  <p>The university community offers a wide range of services to students, ranging from health, counselling, career, housing and legal services, as well as offer facilities for recreation and athletics, and the numerous opportunities for participating in student clubs and associations. Find out more about <a href="http://artsandscience.concordia.ca/currentstudents/studentservices/">student services at Concordia</a>.</p>
  <h2>Academic integrity</h2>
  <p>Concordia University has zero tolerance for cheating and plagiarizing. This means that any student caught doing either will face severe penalties, such as failing a course or even being expelled from the university. Plagiarism is serious business so make sure that you are not found guilty of plagiarizing. Find out more about <a href="http://artsandscience.concordia.ca/currentstudents/academicintegrity/">academic integrity</a>.</p>


