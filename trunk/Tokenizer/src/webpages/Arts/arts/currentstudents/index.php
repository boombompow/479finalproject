  <h2>Arts and Science Learning Centre</h2>
  <p>A state-of-the-art facility for courses using the latest multimedia technology, featuring the Interactive Teaching Studio, the Multimedia Language Studio, and the Distance Learning Studio. Find out more about the <a href="../index.html">Arts &amp; Science Learning Centre</a>.</p>
  <h2>Quebec Residency</h2>
  <p> The Qu&eacute;bec Ministry of Education, Leisure and Sport (MELS) requires universities to collect additional fees from Canadian Citizens and Permanent Residents who enroll in Undergraduate and Graduate programs and who do not qualify as Qu&eacute;bec residents under MELS definitions. Students in special exchange programs, undergraduate French Language or Literature programs, and Doctoral programs may be excluded. Find out more about <a href="http://quebecresidency.concordia.ca/">Quebec residency requirements</a> and the Quebec <a href="http://permanentcode.concordia.ca/">permanent code requirement</a>. </p>

