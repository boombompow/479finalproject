		   <h3>Accessibility Tools</h3>
		   <h4 id="textsize_faq">Text Size</h4>
		   <p>In the top right of every page, we have provided links to three style sheets:</p>
		   <ul>
		     <li>medium (the default setting)</li>
	         <li>large</li>
		     <li>extra-large</li>
	       </ul>
		   <p>These settings allow the user to increase the font size of all pages. This feature requires Javascript to be turned on and  cookies to be permitted. If Javascript is turned off, your browser provides options for increasing text size.</p>
		   <h4>Shortcuts</h4>
		   <p>The access key shortcuts of this site are:</p>
      <ul>
        <li>s - Skip Navigation </li>
        <li> 1- Home Page </li>
        <li>4 - Search </li>
        <li> 0 - Access key details</li>
      </ul>
      <h4>How to use Access Keys on your Browser</h4>
      <ul>
        <li> Internet Explorer 5+ (PC) - Hold down the ALT key, press the access key, release both keys then press ENTER </li>
        <li> Internet Explorer 4 (PC) - Hold down the ALT key and press the access key </li>
        <li> Internet Explorer 5+ (Mac) - Hold down the CTRL key and press the access key </li>
        <li> Internet Explorer 4.5 (Mac) - Access keys are not supported Netscape six and earlier (PC and Mac) Access keys are not supported </li>
        <li> Netscape 7 (PC) - Hold down the ALT key and press the access key </li>
        <li> Firefox, Mozilla (PC) - Hold down SHIFT + ALT key and press the access key </li>
        <li>Firefox, Mozilla (Mac) - Hold down the CTRL key and press the access key</li>
        <li>Safari and Omniweb (Mac) - Hold down the CTRL key and press the access key </li>
        <li> Opera - Hold down the Shift key and press Escape, release both keys, then press the accesskey</li>
  </ul>
</div>
		 <p>In the development of the new Concordia University website, as well as the templates for departmental and unit sites, we have tried to follow accessibility standards in order to make the site usable to the broadest possible audience. We want the site to be usable by people using assistive technologies, such as screen readers, and to those people using older browsers, computers and slow connection speeds.</p>
         <p>In line with the <a href="http://www.w3.org/TR/WCAG/">Web Content Accessibility Guidelines</a>, we have constructed our pages to &quot;transform gracefully&quot;, in other words, we have:</p>
         <ul>
           <li>separated structure from presentation. We use Cascading Style Sheets to control all presentation. Turning off the style sheet will affect the visual presentation but we have tried to ensure that the various site functions and navigation will still work.</li>
           <li>created documents that work even if the user cannot see and/or hear. All content is provided as text; when images, audio or video files are used, they are given equivalent alternatives. Blind users can use screen reader technology to render all text information on a page.</li>
           <li>created documents that do not rely on one type of hardware. Users can access the site with a variety of browser technologies, screen resolutions and settings, with or without mice, etc. See the sections below for information on shortcuts and access keys.</li>
         </ul>
         <p>We have also tried to ensure that the content is understandable and navigable. We have provided:</p>
         <ul>
           <li>a universal top-level tool bar that situates you within the university structure, including links to the appropriate sites in the organization's hierarchy, but also includes &quot;skip to content&quot; and text re-sizing options.</li>
           <li>a standardized navigation structure, using text for links and not images, that clearly situates the user within up to five levels of the site's information architecture.</li>
           <li>a bread crumb trail located just above the page tite, which also situates the user within the site structure.
           </li>
         </ul>
         <p>If you have any feedback on the implementation of the accessibility of this website, please send your comments to the Concordia University Web Communications unit via our <a href="http://www.concordia.ca/about/contactus/feedback.php">online feedback form</a>. Persons using assistive devices should contact the <a href="http://supportservices.concordia.ca/disabilities/">Access Centre for Students with Disabilities</a> for help with assistive technology.</p>
	<h3>The Access Centre for Students with Disabilities</h3>
	<p>The <a href="http://supportservices.concordia.ca/disabilities/">Access Centre for Students with Disabilities</a> provides programs and services which support the integration and independence of students with disabilities into the university community.</p>
	<p>Students with visual or hearing impairments, mobility limitations, or learning disabilities may require special services while studying at Concordia. Services include:</p>
	<ul>
	  <li>orientation sessions</li>
	  <li>individual academic support and advising</li>
	  <li>alternative media transcription</li>
	  <li>volunteer reader services</li>
	  <li>classroom relocation for mobility impaired students</li>
	  <li>tape recorders, FM personal systems, and personal computers with voice and Braille output are available for on campus use.</li>
</ul>
		<h3>Official Policy</h3>
		<p>The <em>Policy on Accessibility for Students with Disabilities</em> outlines the respective roles and responsibilities of the University and of students with disabilities with respect to accessibility to the academic programs and physical facilities of the University. <a href="http://supportservices.concordia.ca/pdf/VRS-14.pdf">Download the policy</a> (PDF).</p>    </div>
